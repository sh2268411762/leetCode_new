package com.gdpph.o1kuaixue.demo.chapter03.section6.section1;

/**
 * 算数运算符
 * @author 零壹学堂
 */
public class MathOperation {
    public static void main(String[] args) {
        int a = 100;
        int b = 10;
        int c = 1010;
        System.out.println("算数运算+：a + b = " + (a + b));
        System.out.println("算数运算-：a - b = " + (a - b));
        System.out.println("算数运算*：a * b = " + (a * b));
        System.out.println("算数运算/：b / a = " + (b / a));
        System.out.println("算数运算%：b % a = " + (b % a));
        System.out.println("算数运算%：c % a = " + (c % a));
    }
}
