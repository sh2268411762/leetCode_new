package com.gdpph.o1kuaixue.demo.chapter07.section2.section3;

/**
 * 数组索引
 * @author 零壹学堂
 */
public class IndexArray {
    public static void main(String[] args) {
        char charArray[] = new char[] { '零', '壹', '学', '堂' };
        System.out.println("索引0位置元素：" + charArray[0]);
        System.out.println("索引1位置元素：" + charArray[1]);
        System.out.println("索引2位置元素：" + charArray[2]);
        System.out.println("索引3位置元素：" + charArray[3]);
    }
}
