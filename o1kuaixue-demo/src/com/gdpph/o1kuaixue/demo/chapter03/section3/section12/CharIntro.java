package com.gdpph.o1kuaixue.demo.chapter03.section3.section12;

/**
 * char字符类型定义
 * @author 零壹学堂
 */
public class CharIntro {
    public static void main(String[] args) {
        char character1 = 'a';
        char character2 = '零';
        char character3 = ' ';
        System.out.println("字符类型数值为：" + character1);
        System.out.println("字符类型数值为：" + character2);
        System.out.println("字符类型数值为：" + character3);
        System.out.println("字符类型当作数字使用：" + (int) character1);
    }
}
