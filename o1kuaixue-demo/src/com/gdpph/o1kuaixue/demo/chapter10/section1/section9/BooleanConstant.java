/**
 * Boolean类中的常量
 */
public class BooleanConstant {
    public static void main(String[] args) {
        Boolean trueConst = Boolean.TRUE;
        Boolean falseConst = Boolean.FALSE;
        Class classType = Boolean.TYPE;
        System.out.println("对应true的对象:" + trueConst);
        System.out.println("对应false的对象" + falseConst);
        System.out.println("对应boolean类型的Class对象" + classType);
    }
}
