package com.gdpph.o1kuaixue.demo.chapter08.section3.section1;

import java.util.regex.*;

/**
 * java.util.regex包介绍
 * @author 零壹学堂
 */
public class RegexIntro {
    public static void main(String[] args) {
        System.out.println("\\\\"); // 输出\\
        System.out.println("\\"); // 输出\
        System.out.println("\n"); // 输出一个换行符
        System.out.println("\\n"); // 输出\n字符而不是换行符
    }
}
