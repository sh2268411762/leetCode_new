package com.gdpph.o1kuaixue.demo.chapter10.section2.section1;

/**
 * Math类三角函数方法
 * @author 零壹学堂
 */
public class TrigonometricMethod {
    public static void main(String[] args) {
        System.out.println("30°正弦值为" + Math.sin(Math.PI / 6));
        System.out.println("90°余弦值为" + Math.cos(Math.PI / 2));
        System.out.println("45°正切值为" + Math.tan(Math.PI / 4));
        System.out.println("60°的弧度为" + Math.toRadians(60));
        System.out.println("π的角度值为" + Math.toDegrees(Math.PI));
    }
}
