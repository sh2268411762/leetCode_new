/**
 * short类型介绍
 * @author 零壹学堂
 */
public class ShortIntro {
    public static void main(String[] args) {
        short number = 12345; // short类型定义
        short x = (short) (12345 + 54321);// 数值溢出short类型最大值
        System.out.println("short类型变量number为：" + number);
        System.out.println("short类型变量x溢出结果为：" + x);
    }
}
