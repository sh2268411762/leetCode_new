/**
 * byte类型定义
 * @author 零壹学堂
 */
public class ByteIntro {
    public static void main(String[] args) {
        byte number = 55; // 定义byte类型变量
        byte x = 10 + 15; // 将10+15=25赋值给x
        System.out.println("byte类型变量number为：" + number);
        System.out.println("byte类型变量x为：" + x);
    }
}
