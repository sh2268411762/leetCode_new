package com.gdpph.o1kuaixue.demo.chapter06.section2.section23;

/**
 * 转为小写字符
 * @author 零壹学堂
 */
public class LowerCaseString {
    public static void main(String[] args) {
        String str = "AbC零壹学堂";
        str = str.toLowerCase();
        System.out.println("将str中大写字母转为小写字母，结果为：" + str);
    }
}
